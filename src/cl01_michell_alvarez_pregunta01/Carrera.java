    private void empezarHilo1() {
        Thread hilo1 = new Thread(new Runnable() {

            int x = 0;

            @Override
            public void run() {

                try {

                    while (x <= 100) {

                        int min = 1;
                        int max = 15;

                        int valor = (int) (Math.random() * (max - min)) + min;
                        x = x + valor;
                        barraDeProgreso1.setValue(x);
                        porcentajeCaballo1.setText(x + "%");
                        if (x > 100) {
                            barraDeProgreso1.setValue(100);
                            porcentajeCaballo1.setText(100 + "%");
                        }

                        x++;
                        contador1 = contador1 + 1;
                        Thread.sleep(100);
                    }

                } catch (Exception e) {
                    System.out.println("Excepci贸n: " + e.getMessage());

                }

            }

        });
        hilo1.start();
    }

